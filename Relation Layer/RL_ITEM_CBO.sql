SELECT
    *
FROM
    (
        SELECT
            IM.COMPANY_ID,
            IM.ITEM_NAME,
            IM.UNIT_LENGTH,
            DL.MAXLENGTH,
            DL.MINLENGTH,
            CASE
                WHEN IM.UNIT_LENGTH > DL.MAXLENGTH THEN 'MAYOR'
                WHEN IM.UNIT_LENGTH < DL.MINLENGTH THEN 'MENOR'
                ELSE 'OK'
            END AS "P_LENGTH",
            IM.UNIT_WIDTH,
            DL.MAXWIDTH,
            DL.MINWIDTH,
            CASE
                WHEN IM.UNIT_WIDTH > DL.MAXWIDTH THEN 'MAYOR'
                WHEN IM.UNIT_WIDTH < DL.MINWIDTH THEN 'MENOR'
                ELSE 'OK'
            END AS "P_WIDTH",
            IM.UNIT_HEIGHT,
            DL.MAXHEIGHT,
            DL.MINHEIGHT,
            CASE
                WHEN IM.UNIT_HEIGHT > DL.MAXHEIGHT THEN 'MAYOR'
                WHEN IM.UNIT_HEIGHT < DL.MINHEIGHT THEN 'MENOR'
                ELSE 'OK'
            END AS "P_HEIGHT",
            IM.UNIT_VOLUME,
            DL.MAXVOLUME,
            DL.MINVOLUME,
            CASE
                WHEN IM.UNIT_VOLUME > DL.MAXVOLUME THEN 'MAYOR'
                WHEN IM.UNIT_VOLUME < DL.MINVOLUME THEN 'MENOR'
                ELSE 'OK'
            END AS "P_VOLUME",
            IM.UNIT_WEIGHT,
            DL.MAXWEIGHT,
            DL.MINWEIGHT,
            CASE
                WHEN IM.UNIT_WEIGHT > DL.MAXWEIGHT THEN 'MAYOR'
                WHEN IM.UNIT_WEIGHT < DL.MINWEIGHT THEN 'MENOR'
                ELSE 'OK'
            END AS "P_WEIGHT",
            SC.CODE_DESC,
            CASE
                IM.COMPANY_ID
                WHEN 129 THEN IW.PROD_CATGRY
                WHEN 77 THEN IW.PROD_CATGRY
                WHEN 127 THEN SC.CODE_DESC
                WHEN 124 THEN IM.PROD_TYPE
                WHEN 100 THEN IM.PROD_TYPE
                WHEN 78 THEN IW.PROD_CATGRY
                ELSE 'NEW'
            END AS CATEGORY,
            IM.BRAND,
            IM.XB_ENVIROMENT
        FROM
            QA_BI.WM.VW_ITEM_CBO_ IM
            JOIN QA_BI.WM.VW_ITEM_WMS_ IW ON IM.ITEM_ID = IW.ITEM_ID
            JOIN SIQC.SCIQ_XBWM19WM.DIMENSION_LIMITS DL ON IM.COMPANY_ID = DL.COMPANY_ID
            JOIN WM.GOLD.SYS_CODE SC ON SC.CODE_ID = IW.PROD_CATGRY
            AND sc.code_type = '060'
            AND sc.rec_type = 'B'
            AND (
                (
                    im.company_id = 127
                    AND sc.code_desc IN (
                        'LCN - FASHION ZONE',
                        'LCN - FOOTWEAR ZONE',
                        'LCN - JEWERLY ZONE',
                        'LCN - SWIMWEAR'
                    )
                )
                OR im.company_id <> 127
            )
        WHERE
            IM.COMPANY_ID IN (
                124,
                100,
                129,
                77,
                127
            )
            AND IM.XB_ENVIROMENT = 'GOLD'
            AND IW.XB_ENVIROMENT = 'GOLD'
            AND IM.ARCHIVE_DTTM IS NULL
            AND IW.ARCHIVE_DTTM IS NULL
    ) DB
WHERE
    "P_LENGTH" != 'OK'
    OR "P_WIDTH" != 'OK'
    OR "P_HEIGHT" != 'OK'
    OR "P_VOLUME" != 'OK'
    OR "P_WEIGHT" != 'OK'
ORDER BY
    2;
